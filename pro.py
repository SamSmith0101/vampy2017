import turtle as t 

t.speed(5)

t.shape("turtle")

def dog():
	t.forward(30)
	t.left(90)
	t.forward(30)
	t.left(90)
	t.forward(30)
	t.left(90)
	t.forward(30)
	t.forward(45)
	t.right(90)
	t.forward(60)
	t.right(90)
	t.forward(45)
	t.right(90)
	t.forward(60)
	t.right(90)
	t.forward(95)
	t.right(90)
	t.forward(10)
	t.right(90)
	t.forward(40)
	t.left(90)
	t.forward(5)
	t.left(90)
	t.forward(40)

dog()

t.exitonclick()
